import argparse
import json
import random
from pathlib import Path
import pandas as pd

def truncate_text(s: str, percentage: float, rel_location: float) -> str:
    total_chars = len(s)
    if total_chars == 0: return s
    chars_to_remove = int((percentage / 100) * total_chars)
    start_pos = int(total_chars * rel_location)
    start_truncate = max(0, start_pos - chars_to_remove // 2)
    end_truncate = min(total_chars, start_truncate + chars_to_remove)
    return s[:start_truncate] + s[end_truncate:]

def get_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("-p", type=float, default=0.3, help="Proportion of duplicate labels in [0,1]")
    parser.add_argument("-o", "--output", type=str, default="lshbloom_dataset", help="Output basename")
    return parser.parse_args()

def main():
    args = get_args()
    out_dir = Path("benchmark_dfs")
    out_dir.mkdir(exist_ok=True, parents=True)

    jsonl_files = [
        "synthetic_benchmark/arxiv_html_reference_v2_neardup_id_text.jsonl",
        "synthetic_benchmark/arxiv_tesseract_ocr_all_id_text.jsonl",
        "synthetic_benchmark/pymupdf_id_text.jsonl",
        "synthetic_benchmark/pypdf_id_text.jsonl"
    ]

    all_data = {}
    for fpath in jsonl_files:
        parser_name = Path(fpath).name.split('_')[0]
        if not Path(fpath).exists(): continue
        
        with open(fpath, "r", encoding="utf-8") as f:
            for line in f:
                rec = json.loads(line)
                doc_id = rec["id"]
                if doc_id not in all_data:
                    all_data[doc_id] = []
                
                # Fetch metadata generated by extract_id_text_jsonl.py
                meta = rec.get("meta", {})
                
                all_data[doc_id].append({
                    "id": doc_id, 
                    "text": rec["text"], 
                    "parser": parser_name,
                    "meta": meta
                })

    final_jsonl_records = []
    csv_records = []
    
    target_total = 4000
    target_dups = int(target_total * args.p)
    target_originals = target_total - target_dups

    sampled_ids = random.sample(list(all_data.keys()), min(target_originals, len(all_data)))
    
    # helper to easily build metadata columns for CSV
    def build_csv_row(row_id, is_dup, mod_type, src_doc_id, doc_entry):
        base = {
            "id": row_id,
            "is_duplicate": is_dup,
            "modification": mod_type,
            "source_doc_id": src_doc_id,
        }
        # Unpack metadata fields
        meta = doc_entry.get("meta", {})
        base["doc_id"] = meta.get("doc_id", "")
        base["parser_name"] = meta.get("parser_name", "")
        base["source_type"] = meta.get("source_type", "")
        base["title"] = meta.get("title", "")
        base["primary_category"] = meta.get("primary_category", "")
        base["published"] = meta.get("published", "")
        
        # Flatten deep nested 'metadata' if present
        deep_meta = meta.get("metadata", {})
        base["source"] = deep_meta.get("source", "")
        base["html_available"] = deep_meta.get("html_available", "")
        base["categories"] = str(deep_meta.get("categories", []))
        return base

    original_docs_in_benchmark = {} 
    for doc_id in sampled_ids:
        doc = random.choice(all_data[doc_id])
        row_id = f"{doc_id}_orig"
        final_jsonl_records.append({"id": row_id, "text": doc["text"]})
        csv_records.append(build_csv_row(row_id, 0, "none", doc_id, doc))
        original_docs_in_benchmark[doc_id] = row_id

    dup_candidates = list(original_docs_in_benchmark.keys())
    current_dups = 0

    while current_dups < target_dups and dup_candidates:
        doc_id = random.choice(dup_candidates)
        orig_row_id = original_docs_in_benchmark[doc_id]
        
        if len(all_data[doc_id]) > 1 and random.random() > 0.5:
            doc = random.choice(all_data[doc_id])
            row_id = f"{doc_id}_pdup_{current_dups}"
            final_jsonl_records.append({"id": row_id, "text": doc["text"]})
            csv_records.append(build_csv_row(row_id, 1, "parser", orig_row_id, doc))
        else:
            doc = all_data[doc_id][0]
            trunc_text = truncate_text(doc["text"], percentage=random.choice([1,2,5,7,10]), rel_location=random.choice([0, 0.25, 0.5, 0.75, 1.0]))
            row_id = f"{doc_id}_tdup_{current_dups}"
            final_jsonl_records.append({"id": row_id, "text": trunc_text})
            csv_records.append(build_csv_row(row_id, 1, "truncation", orig_row_id, doc))
            
        current_dups += 1

    combine = list(zip(final_jsonl_records, csv_records))
    random.shuffle(combine)
    final_jsonl_records, csv_records = zip(*combine) if combine else ([], [])

    out_jsonl = out_dir / f"{args.output}.jsonl"
    out_csv = out_dir / f"{args.output}.csv"

    with open(out_jsonl, "w", encoding="utf-8") as f:
        for r in final_jsonl_records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
            
    df = pd.DataFrame(csv_records)
    df.to_csv(out_csv, sep='|', index=False)

    print(f"Created {len(final_jsonl_records)} total records.")
    print(f"-> {out_jsonl.resolve()}")
    print(f"-> {out_csv.resolve()}")

if __name__ == "__main__":
    random.seed(42)
    main()